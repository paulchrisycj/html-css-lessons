// If-Else Statements
const color = "red"
if(color == 'green') {
    console.log("Go")
}else{
    console.log("Stop")
}

const myAge = 20
if(myAge > 18){
    console.log("You may enter")
}else{
    console.log("You SHALL NOT PASS")
}

